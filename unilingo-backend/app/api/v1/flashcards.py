"""
Flashcard API routes
"""
from uuid import UUID
from datetime import datetime, timedelta, timezone
from fastapi import APIRouter, Depends, HTTPException, status
from sqlalchemy import select, func
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy.orm import selectinload

from app.database import get_db
from app.api.deps import get_current_user
from app.models.user import User
from app.models.flashcard import FlashcardDeck, Flashcard, FlashcardReview
from app.models.vocabulary import VocabularyNote
from app.schemas.flashcard import (
    CreateDeckRequest, UpdateDeckRequest,
    DeckResponse, DeckListResponse, DeckDetailResponse,
    AddCardRequest, CardResponse,
    ReviewCardRequest, ReviewCardResponse,
    StudySessionResponse,
    AutoGenerateDeckRequest,
)

router = APIRouter(prefix="/flashcards", tags=["Flashcards"])


@router.get("/decks", response_model=DeckListResponse)
async def list_decks(
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """List all flashcard decks for the current user."""
    result = await db.execute(
        select(FlashcardDeck)
        .where(FlashcardDeck.user_id == current_user.id)
        .order_by(FlashcardDeck.updated_at.desc())
    )
    decks = result.scalars().all()
    return DeckListResponse(
        items=[DeckResponse.model_validate(d) for d in decks],
        total=len(decks),
    )


@router.post("/decks", response_model=DeckResponse, status_code=status.HTTP_201_CREATED)
async def create_deck(
    request: CreateDeckRequest,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """Create a new flashcard deck."""
    deck = FlashcardDeck(
        user_id=current_user.id,
        title=request.title,
        description=request.description,
        is_public=request.is_public,
    )
    db.add(deck)
    await db.flush()
    return DeckResponse.model_validate(deck)


@router.get("/decks/{deck_id}", response_model=DeckDetailResponse)
async def get_deck_detail(
    deck_id: UUID,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """Get deck detail with all cards."""
    result = await db.execute(
        select(FlashcardDeck)
        .options(selectinload(FlashcardDeck.cards))
        .where(
            FlashcardDeck.id == deck_id,
            FlashcardDeck.user_id == current_user.id,
        )
    )
    deck = result.scalar_one_or_none()
    if not deck:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Deck not found")

    return DeckDetailResponse(
        deck=DeckResponse.model_validate(deck),
        cards=[CardResponse.model_validate(c) for c in deck.cards],
    )


@router.patch("/decks/{deck_id}", response_model=DeckResponse)
async def update_deck(
    deck_id: UUID,
    request: UpdateDeckRequest,
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """Update a flashcard deck."""
    result = await db.execute(
        select(FlashcardDeck).where(
            FlashcardDeck.id == deck_id,
            FlashcardDeck.user_id == current_user.id,
        )
    )
    deck = result.scalar_one_or_none()
    if not deck:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Deck not found")

    for field, value in request.model_dump(exclude_unset=True).items():
        setattr(deck, field, value)
    await db.flush()
    return DeckResponse.model_validate(deck)


